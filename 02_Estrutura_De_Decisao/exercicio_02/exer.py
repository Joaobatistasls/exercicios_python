# Na tela se o valor é positivo ou negativo.

valor_desejado = int(input("Me informe um valor: "))

if (valor_desejado > 1): 
    print(f"O número foi {valor_desejado}. Ele é Positivo!")
elif (valor_desejado < 1):
    print(f"O número foi {valor_desejado}. Ele é Negativo!")

    