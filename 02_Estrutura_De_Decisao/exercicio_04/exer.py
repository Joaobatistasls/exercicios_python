# Faça um Programa que verifique se uma letra digitada é vogal ou consoante.

letra_digitada = input("Me informe uma letra? ")

if (letra_digitada == "a"):
    print("Vogal")
elif (letra_digitada == "e"):
    print("Vogal")
elif (letra_digitada == "i"):
    print("Vogal")
elif (letra_digitada == "o"):
    print("Vogal")
elif (letra_digitada == "u"):
    print("Vogal")
else:
    print("Consoante")
