# Faça um Programa que peça dois números e imprima a soma.

# Inputs para fazer as perguntas: 
numero_1 = input("Me informe um número: ")
numero_2 = input("Me informe outro número: ")

# Int pra inverte o type dos inputs:
numero_1_somar = int(numero_1)
numero_2_somar = int(numero_2)

# Variável para somar os números: 
somar_numeros = numero_1_somar + numero_2_somar

# Print pra exibir os números:
print(f"A somar dos números é: {somar_numeros}")
